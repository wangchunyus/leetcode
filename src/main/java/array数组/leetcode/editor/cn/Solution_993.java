    /**
     * Definition for a binary tree node.
     * public class TreeNode {
     * int val;
     * TreeNode left;
     * TreeNode right;
     * TreeNode() {}
     * TreeNode(int val) { this.val = val; }
     * TreeNode(int val, TreeNode left, TreeNode right) {
     * this.val = val;
     * this.left = left;
     * this.right = right;
     * }
     * }
     */
    class Solution {

        int xDept;
        int yDept;
        public boolean isCousins(TreeNode root, int x, int y) {

            Map<Integer, Integer> map = new HashMap<>();
            dfs(0, root, new TreeNode(-1), x, y, map);
            if (xDept == yDept && map.get(x) != map.get(y)) {
                return true;
            }
            return false;
        }

        private void dfs(int dept, TreeNode root, TreeNode pNode, int x, int y, Map<Integer, Integer> map) {
            if (root == null) {
                return;
            }
            if (root.val == x) {
                map.put(x, pNode.val);
                xDept = dept;
            } else if (root.val == y) {
                map.put(y, pNode.val);
                yDept = dept;
            }
            dfs(dept+1, root.left, root, x, y, map);
            dfs(dept+1, root.right, root, x, y, map);
        }
    }
